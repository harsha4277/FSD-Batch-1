package com.dl.one;

public class Eg4 {

	public static void main(String[] args) {
		
		int a= 10; //0 to 9 // base 10
		int b = 0107; // oct // 1 to 7 // base 8
		System.out.println(a);
		System.out.println(b);
		
		int c = 0xaf;  // a to f and 0 to 9 // base 16
		System.out.println(c);
		
		int d = 0b0101010101;
		System.out.println(d); //  0 1 // base 2

	}

}
