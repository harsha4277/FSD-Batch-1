package com.dl.one;

public class Eg4 {

	public static void main(String[] args) {
		
		int userSalary = 80000;
		
		switch (userSalary) {
		case 10000:
			System.out.println("User Salary is 10000");
			break;
		case 20000:
			System.out.println("User Salary is 20000");
			break;
		case 30000:
			System.out.println("User Salary is 30000");
			break;
		case 40000:
			System.out.println("User Salary is 40000");
			break;
		case 50000:
			System.out.println("User Salary is 50000");
			break;

		default:
			System.out.println("Not Validated");
			break;
		}

	}

}
