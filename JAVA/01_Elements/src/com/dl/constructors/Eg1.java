package com.dl.constructors;

public class Eg1 {
	
	public Eg1() {
		System.out.println("Default Constructor");
	}

	public static void main(String[] args) {
		
		Eg1 eg1 = new Eg1(); // new object
		System.out.println(eg1);
		Eg1 eg12 = new Eg1(); // new object
		System.out.println(eg12);
		Eg1 eg13 = new Eg1(); // new object
		System.out.println(eg13);
	}

}
