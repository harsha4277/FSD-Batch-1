//String Literals
let firstName = "Sai"
let lastName = "Kiran"

console.log(firstName) //Sai
console.log(lastName) //Kiran

//Object literals
let user = {
    firstName : "Sai",
    lastName : "Kiran",
}
console.log(user) // { firstName: 'Sai', lastName: 'Kiran' }

//String literals
let fName = "Sai"
let lName = "Kiran"

//Object literals
let customer = {
    fName : fName,
    lName : lName
}
console.log(customer) //{ fName: 'Sai', lName: 'Kiran' }

let c = {
    fName,
    lName
}
console.log(c) //{ fName: 'Sai', lName: 'Kiran' }