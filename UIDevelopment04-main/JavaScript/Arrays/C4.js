//push and pop 
let names = ["A", "B", "C", "D", "E", "F"]
console.log(names) // [ 'A', 'B', 'C', 'D', 'E', 'F' ]
console.log(names.length) // 6
console.log(names.push("G")) // 7
console.log(names.push("H")) // 8
console.log(names.push("I")) // 9
console.log(names)
console.log(names.length) // 9

//pop 
let colors = ["A", "B", "C", "D", "E", "F"]
console.log(colors) // [ 'A', 'B', 'C', 'D', 'E', 'F' ]
console.log(colors.pop()) // F
console.log(colors.pop()) // E
console.log(colors) // [ 'A', 'B', 'C', 'D' ]

